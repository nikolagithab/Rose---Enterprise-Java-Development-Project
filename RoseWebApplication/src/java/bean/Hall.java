public class Hall {
   private int hallID;
   private String hallName;

   public int getHallID()
   {
      return hallID;
   }

   public void setHallID(int hallID)
   {
      this.hallID = hallID;
   }

   public String getHallName()
   {
      return hallName;
   }

   public void setHallName(String hallName)
   {
      this.hallName = hallName;
   }
}
